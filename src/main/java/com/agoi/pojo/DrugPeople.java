package com.agoi.pojo;

public class DrugPeople {
    private Integer drId;

    private Integer peopleId;

    private Integer drNumber;

    private Integer dredNumber;

    public Integer getDrId() {
        return drId;
    }

    public void setDrId(Integer drId) {
        this.drId = drId;
    }

    public Integer getPeopleId() {
        return peopleId;
    }

    public void setPeopleId(Integer peopleId) {
        this.peopleId = peopleId;
    }

    public Integer getDrNumber() {
        return drNumber;
    }

    public void setDrNumber(Integer drNumber) {
        this.drNumber = drNumber;
    }

    public Integer getDredNumber() {
        return dredNumber;
    }

    public void setDredNumber(Integer dredNumber) {
        this.dredNumber = dredNumber;
    }
}